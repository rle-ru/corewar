#include <unistd.h>

void	ft_putendl(char const *s)
{
	unsigned int	i;

	if (s != 0)
	{
		i = 0;
		while (s[i++])
			write(1, &s[i - 1], 1);
		write(1, "\n", 1);
	}
}
