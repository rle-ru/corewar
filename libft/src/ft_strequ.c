int		ft_strequ(char const *s1, char const *s2)
{
	int		i;

	if (s1 != 0 && s2 != 0)
	{
		i = 0;
		while (s1[i])
		{
			if (!s2[i] || (s2[i] != s1[i]))
				return (0);
			i++;
		}
		if (s2[i])
			return (0);
		return (1);
	}
	return (0);
}
