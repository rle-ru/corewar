#include <unistd.h>

void	ft_putendl_fd(char const *s, int fd)
{
	unsigned int i;

	if (s != 0 && fd >= 0)
	{
		i = 0;
		while (s[i++])
			write(fd, &s[i - 1], 1);
		write(fd, "\n", 1);
	}
}
